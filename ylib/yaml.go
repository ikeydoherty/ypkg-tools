package ylib

import (
	"github.com/go-yaml/yaml"
	"io/ioutil"
)

// MarshalledYpkg is autogenerated by the tooling solely for writing to disk
type MarshalledYpkg struct {
	Name        string
	Version     string
	Homepage    string
	License     []string
	Summary     string
	Description string
	Source      map[string]string
}

// WriteYpkg will attempt to write the MarshalledYpkg to a file
func WriteYpkg(path string, pkg *MarshalledYpkg) error {
	var output []byte
	var err error

	if output, err = yaml.Marshal(pkg); err != nil {
		return err
	}

	return ioutil.WriteFile(path, output, 00644)
}
